# This is the inference module that replaces the LLM-based vision agent
# with your custom trained model.

def create_custom_vision_agent():
    """
    Generate the custom VisionAgent that uses YOUR trained model
    instead of an LLM for floor plan analysis.
    """
    
    agent_code = '''
import numpy as np
import cv2
import uuid
import math
from typing import List, Optional
from ultralytics import YOLO
from backend.agents.vision.schema import VisionExtraction, DetectedWall, DetectedOpening
from backend.core.bim_state import BIMElement, ObjectType, Vector3


# Class mapping (must match training)
CLASS_NAMES = {0: "wall", 1: "door", 2: "window", 3: "room", 
               4: "stairs", 5: "bathroom_fixture", 6: "kitchen_fixture"}


class CustomVisionAgent:
    """
    Custom-trained vision model for floor plan analysis.
    Uses YOLOv11-seg instead of LLM for deterministic, fast detection.
    
    Advantages over LLM:
    - 10-50x faster inference
    - Deterministic results (same input = same output)
    - No API costs 
    - Works offline
    - Pixel-accurate segmentation masks
    """
    
    def __init__(self, model_path: str = "models/floorplan_best.pt"):
        self.model = YOLO(model_path)
        self.confidence_threshold = 0.4
        self.iou_threshold = 0.5
        # Pixels-to-meters scale (calibrate with known measurements)
        self.scale = 0.02  # Default: 1 pixel = 0.02 meters (50px per meter)
    
    def _estimate_scale(self, image: np.ndarray, detections) -> float:
        """
        Estimate pixel-to-meter scale from detected elements.
        Uses standard door width (0.8m) as reference.
        """
        door_widths_px = []
        for det in detections:
            if det["class"] == "door":
                w = det["bbox"][2] - det["bbox"][0]
                h = det["bbox"][3] - det["bbox"][1]
                door_widths_px.append(min(w, h))  # Door width is shorter dimension
        
        if door_widths_px:
            avg_door_px = np.mean(door_widths_px)
            # Standard door = 0.8m
            self.scale = 0.8 / avg_door_px
            
        return self.scale
    
    async def process_plan(self, image_bytes: bytes) -> VisionExtraction:
        """Process floor plan image and extract geometry."""
        
        # 1. Decode image
        is_pdf = image_bytes.startswith(b"%PDF")
        if is_pdf:
            import fitz
            doc = fitz.open(stream=image_bytes, filetype="pdf")
            page = doc.load_page(0)
            pix = page.get_pixmap(matrix=fitz.Matrix(2, 2))
            img_array = np.frombuffer(pix.samples, dtype=np.uint8).reshape(
                pix.height, pix.width, pix.n
            )
            doc.close()
        else:
            img_array = np.frombuffer(image_bytes, dtype=np.uint8)
            img_array = cv2.imdecode(img_array, cv2.IMREAD_COLOR)
        
        # 2. Run inference
        results = self.model.predict(
            img_array,
            conf=self.confidence_threshold,
            iou=self.iou_threshold,
            imgsz=1024,
            verbose=False
        )[0]
        
        # 3. Parse detections
        detections = []
        for i, box in enumerate(results.boxes):
            cls_id = int(box.cls[0])
            conf = float(box.conf[0])
            x1, y1, x2, y2 = box.xyxy[0].tolist()
            
            det = {
                "class": CLASS_NAMES.get(cls_id, "unknown"),
                "confidence": conf,
                "bbox": [x1, y1, x2, y2],
                "mask": None
            }
            
            # Get segmentation mask if available
            if results.masks is not None and i < len(results.masks):
                det["mask"] = results.masks[i].xy[0].tolist()  # Polygon points
            
            detections.append(det)
        
        # 4. Estimate scale from door detections
        scale = self._estimate_scale(img_array, detections)
        
        # 5. Convert to VestaAgent format
        walls = []
        openings = []
        room_labels = []
        
        for det in detections:
            x1, y1, x2, y2 = det["bbox"]
            cx = (x1 + x2) / 2 * scale  # Center X in meters
            cy = (y1 + y2) / 2 * scale  # Center Y in meters
            w = (x2 - x1) * scale
            h = (y2 - y1) * scale
            
            if det["class"] == "wall":
                # Determine wall orientation from aspect ratio
                if w > h:
                    # Horizontal wall
                    walls.append(DetectedWall(
                        start=[cx - w/2, cy],
                        end=[cx + w/2, cy],
                        thickness=max(h, 0.1),
                        is_load_bearing=(h > 0.15)  # Thick walls are load-bearing
                    ))
                else:
                    # Vertical wall
                    walls.append(DetectedWall(
                        start=[cx, cy - h/2],
                        end=[cx, cy + h/2],
                        thickness=max(w, 0.1),
                        is_load_bearing=(w > 0.15)
                    ))
            
            elif det["class"] == "door":
                openings.append(DetectedOpening(
                    type="door",
                    position=[cx, cy],
                    width=min(w, h),  # Door width is shorter dimension
                    rotation=0 if w > h else 90
                ))
            
            elif det["class"] == "window":
                openings.append(DetectedOpening(
                    type="window",
                    position=[cx, cy],
                    width=max(w, h),  # Window width is longer dimension
                    rotation=0 if w > h else 90
                ))
            
            elif det["class"] == "room":
                room_labels.append(f"Room at ({cx:.1f}, {cy:.1f})")
        
        # 6. Build extraction result
        avg_confidence = np.mean([d["confidence"] for d in detections]) if detections else 0.5
        
        notes = (
            f"Custom model detected {len(walls)} walls, "
            f"{len([o for o in openings if o.type == 'door'])} doors, "
            f"{len([o for o in openings if o.type == 'window'])} windows. "
            f"Rooms found: {', '.join(room_labels) if room_labels else 'N/A'}. "
            f"Total: {len(room_labels)} rooms. "
            f"Scale: {scale:.4f} m/px."
        )
        
        return VisionExtraction(
            walls=walls if walls else [DetectedWall(start=[0,0], end=[10,0], thickness=0.2, is_load_bearing=True)],
            openings=openings,
            scale_ratio=scale,
            confidence_score=avg_confidence,
            notes=notes
        )
'''
    
    output_path = "backend/agents/vision/custom_agent.py"
    with open(output_path, "w") as f:
        f.write(agent_code)
    print(f"✅ Custom vision agent written to {output_path}")


