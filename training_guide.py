def evaluate_model(model_path: str, dataset_yaml: str):
    """Evaluate trained model on test set."""
    from ultralytics import YOLO
    
    model = YOLO(model_path)
    metrics = model.val(data=dataset_yaml, split="test")
    
    print(f"\n📊 Evaluation Results:")
    print(f"   mAP@50:     {metrics.box.map50:.4f}")
    print(f"   mAP@50-95:  {metrics.box.map:.4f}")
    print(f"   Precision:   {metrics.box.p.mean():.4f}")  
    print(f"   Recall:      {metrics.box.r.mean():.4f}")
    
    # Per-class results
    print(f"\n   Per-Class mAP@50:")
    for i, name in CLASSES.items():
        if i < len(metrics.box.ap50):
            print(f"   {name:20s}: {metrics.box.ap50[i]:.4f}")
    
    return metrics


def export_model(model_path: str, format: str = "onnx"):
    """
    Export model for deployment.
    
    Formats:
    - onnx:     CPU inference, no GPU needed (recommended for VestaAgent)
    - torchscript: PyTorch deployment
    - engine:   TensorRT (NVIDIA GPU, fastest)
    - openvino: Intel CPU optimized
    - coreml:   Apple Silicon
    """
    from ultralytics import YOLO
    
    model = YOLO(model_path)
    
    exported = model.export(
        format=format,
        imgsz=1024,
        half=False,       # FP32 for CPU (use True for GPU)
        simplify=True,    # Simplify ONNX graph
        dynamic=True,     # Dynamic batch size
    )
    
    print(f"✅ Model exported to: {exported}")
    return exported


