def train_model(
    dataset_yaml: str = "floorplan_dataset.yaml",
    model_size: str = "m",         # n=nano, s=small, m=medium, l=large, x=xlarge
    epochs: int = 150,
    batch_size: int = 16,
    img_size: int = 1024,          # Floor plans need high resolution
    task: str = "segment",         # "detect" for boxes, "segment" for masks
    device: str = "0",             # GPU device, "cpu" for CPU training
    resume: bool = False,
):
    """
    Train YOLOv11 on floor plan dataset.
    
    Recommended configurations by hardware:
    
    ┌─────────────┬───────────┬──────────┬──────────┬───────────┐
    │ GPU         │ Model     │ Batch    │ ImgSize  │ Time      │
    ├─────────────┼───────────┼──────────┼──────────┼───────────┤
    │ RTX 3060 6G │ yolo11s   │ 8        │ 640      │ ~6 hours  │
    │ RTX 3080 10G│ yolo11m   │ 16       │ 1024     │ ~8 hours  │
    │ RTX 4090 24G│ yolo11l   │ 32       │ 1280     │ ~5 hours  │
    │ A100 40G    │ yolo11x   │ 64       │ 1280     │ ~3 hours  │
    │ CPU only    │ yolo11n   │ 4        │ 640      │ ~48 hours │
    │ Google Colab│ yolo11m   │ 16       │ 1024     │ ~12 hours │
    └─────────────┴───────────┴──────────┴──────────┴───────────┘
    """
    from ultralytics import YOLO

    # Load pretrained model (transfer learning from COCO)
    if task == "segment":
        model = YOLO(f"yolo11{model_size}-seg.pt")  # Segmentation model
    else:
        model = YOLO(f"yolo11{model_size}.pt")        # Detection model

    # Train
    results = model.train(
        data=dataset_yaml,
        epochs=epochs,
        batch=batch_size,
        imgsz=img_size,
        device=device,
        
        # Optimizer
        optimizer="AdamW",
        lr0=0.001,           # Initial learning rate
        lrf=0.01,            # Final learning rate factor
        weight_decay=0.0005,
        warmup_epochs=5,
        
        # Augmentation (floor-plan specific)
        degrees=90.0,        # Critical: floor plans can be any orientation
        flipud=0.0,          # No vertical flip
        fliplr=0.5,          # Horizontal mirror is OK
        mosaic=1.0,
        
        # Architecture
        close_mosaic=15,     # Disable mosaic last 15 epochs for fine-tuning
        
        # Output
        project="runs/floorplan",
        name=f"yolo11{model_size}_{task}_{epochs}ep",
        
        # Performance
        workers=8,
        cache=True,          # Cache images in RAM for speed
        amp=True,            # Mixed precision training
        
        # Resume from checkpoint
        resume=resume,
        
        # Validation
        val=True,
        plots=True,          # Generate training plots
        save=True,
        save_period=10,      # Save checkpoint every 10 epochs
    )
    
    print(f"\n✅ Training complete!")
    print(f"📊 Best mAP@50: {results.results_dict.get('metrics/mAP50(B)', 'N/A')}")
    print(f"📁 Best model: {results.save_dir}/weights/best.pt")
    
    return results


